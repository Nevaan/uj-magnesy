    def underTest = new MCSimulation()
    static long start, end

    static def TIME = 300000
    static int STEPS = 50_000
    static int DIV = 20

    static int REPEATS = 1

    @Shared
    def static resultsMap = [:]

    def setupSpec() {
        start = System.currentTimeMillis()
    }
    def cleanupSpec() {
        end = System.currentTimeMillis()

        resultsMap = resultsMap.sort()

        def resultAsString = []

        resultsMap.each {it -> resultAsString << it.value}

        def filename = "results.txt"
        File file = new File(filename)
        file.write("Test results:" + resultAsString.join("\n") + "\n\n\n" + "Time in ms: ${end - start}\n" + "Time in s: ${(end - start)/1000}\n" + "Time in m: ${((end - start) / 1000) / 60}\n")

    }


    def initTestClass(int[][] lattice, double T) {
        underTest.setLattice(lattice, 500)
        underTest.setEnergyParameters([0.0 as Double, 1.0 as Double], 0.0)
        underTest.setProbabilityFormula(Simulation.ProbabilityFormula.GLAUBER)
        underTest.setTemperatureBoltzmannConstant(T)
    }

    Tuple4<Integer, Double, Double, Double> oneSimulation(MCSimulation sim) {
        def stepsTaken = 0
        long endTime = System.currentTimeMillis() + TIME
        do {
            sim.executeMCSteps(STEPS)
            stepsTaken += 1
        } while (System.currentTimeMillis() < endTime)

        return new Tuple(stepsTaken * STEPS, ((sim.getState().totalEnergy()) / (DIV*DIV)), (sim.getState().orderParameter()), (sim.getState().nearestNeighbourOrder()))
    }

    def handleResults(double tkb, double expected, List<Tuple4<Integer, Double, Double, Double>> results) {
        def executionResults = results.collect {it[1]}

        def averageResult = executionResults.value.sum() / REPEATS

        def header = "\ntemperatureBoltzmannConstant = ${tkb} (${expected} expected)\n"
        def resultsAsString = results.collect {it -> "\t Result: ${it[1]} (${it[0]} steps, ${it[2]} order parameter, ${it[3]} nearest neighbour order)"}.join("\n")
        def averageAsString = "\n\n\t Average: ${averageResult}"

        resultsMap[tkb] = "${header}${resultsAsString}${averageAsString}"
        return true
    }

