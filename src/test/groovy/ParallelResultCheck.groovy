    static long start, end

    static def TIME = 300000 / 5
    static int STEPS = 100
    static int DIV = 20

    static int REPEATS = 1

    def setupSpec() {
        start = System.currentTimeMillis()
        println "starting... ${start}"
    }
    def cleanupSpec() {
        end = System.currentTimeMillis()
        println "ending...${end}"
        println "Time in ms: ${end - start}"
        println "Time in s: ${(end - start)/1000}"
        println "Time in m: ${((end - start)/1000)/60}"
    }


    def initTestClass(int[][] lattice, double T) {
        underTest.setLattice(lattice, 500)
        underTest.setEnergyParameters([0.0 as Double, 1.0 as Double], 0.0)
        underTest.setProbabilityFormula(Simulation.ProbabilityFormula.GLAUBER)
        underTest.setTkB(T)
    }

    double oneSimulation(MCSimulation sim, String tkB) {
        def stepsTaken = 0
        long endTime = System.currentTimeMillis() + TIME
        do {
            sim.executeMCSteps(STEPS)
            stepsTaken += 1
        } while (System.currentTimeMillis() < endTime)

        println "[${tkB}] Steps taken: ${stepsTaken * STEPS}"
        return ((sim.getState().totalEnergy()) / (DIV*DIV))
    }


